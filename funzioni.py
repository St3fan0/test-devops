# funzioni

def somma(a, b):
    v1 = int(a)
    v2 = int(b)
    print("Somma dei due numeri forniti = " + str(v1 + v2))

def differenza(a, b):
    v1 = int(a)
    v2 = int(b)
    print("differenza dei due numeri forniti = " + str(v1 - v2))

def prodotto(a, b):
    v1 = int(a)
    v2 = int(b)
    print("prodotto dei due numeri forniti = " + str(v1 * v2))

def divisione(a, b):
    v1 = int(a)
    v2 = int(b)
    print("divisione dei due numeri forniti = " + str(v1 / v2))